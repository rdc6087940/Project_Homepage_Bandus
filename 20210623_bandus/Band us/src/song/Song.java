package song;

public class Song {
	
	private int song_no;
	private String song_name;
	private String song_date;
	private String artist_name;
	private String song_url;
	private String album_name;
	private String song_comment;
	private int viewnum;
	private double song_like;
	private String song_imgurl;
	
	
	public int getSong_no() {
		return song_no;
	}
	public void setSong_no(int song_no) {
		this.song_no = song_no;
	}
	public String getSong_name() {
		return song_name;
	}
	public void setSong_name(String song_name) {
		this.song_name = song_name;
	}
	public String getSong_date() {
		return song_date;
	}
	public void setSong_date(String song_date) {
		this.song_date = song_date;
	}
	public String getArtist_name() {
		return artist_name;
	}
	public void setArtist_name(String artist_name) {
		this.artist_name = artist_name;
	}
	public String getSong_url() {
		return song_url;
	}
	public void setSong_url(String song_url) {
		this.song_url = song_url;
	}
	public String getAlbum_name() {
		return album_name;
	}
	public void setAlbum_name(String album_name) {
		this.album_name = album_name;
	}
	public String getSong_comment() {
		return song_comment;
	}
	public void setSong_comment(String song_comment) {
		this.song_comment = song_comment;
	}
	public int getViewnum() {
		return viewnum;
	}
	public void setViewnum(int viewnum) {
		this.viewnum = viewnum;
	}
	public double getSong_like() {
		return song_like;
	}
	public void setSong_like(double song_like) {
		this.song_like = song_like;
	}
	public String getSong_imgurl() {
		return song_imgurl;
	}
	public void setSong_imgurl(String song_imgurl) {
		this.song_imgurl = song_imgurl;
	}
	
	

}
